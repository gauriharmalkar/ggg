package refactoring;

public class g {
	public static void main(String[] args, int newParam){
		int[] marks123={1,2,3};
		int markss=10;
		marki12t(marks123);
		System.out.println(markss*10);
		System.out.println(markss*10);
		
	}

	public static void marki12t(int[] marks) {
		for(int mark:marks)
		{
			System.out.println(mark);
		}
	}


}
